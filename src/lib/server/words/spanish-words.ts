// Filter and normalize: only keep words of exact lengths 4–7, lowercase, no accents
const normalize = (w: string) =>
	w
		.toLowerCase()
		.normalize('NFD')
		.replace(/[\u0300-\u036f]/g, '')
		.replace(/[^a-z]/g, '');

export const wordsByLength: Record<number, string[]> = { 4: [], 5: [], 6: [], 7: [] };

for (const raw of spanishWords) {
	const w = normalize(raw);
	const len = w.length;
	if (len >= 4 && len <= 7) {
		if (!wordsByLength[len].includes(w)) {
			wordsByLength[len].push(w);
		}
	}
}
